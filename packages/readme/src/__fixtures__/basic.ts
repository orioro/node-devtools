/**
 * Adds two numbers together.
 * @public
 * @param a - first number
 * @param b - second number
 * @returns the sum
 * @example
 * add(1, 2) // 3
 */
export function add(a: number, b: number): number {
  return a + b
}

/**
 * Not included (no tag).
 */
export function internal(x: string): string {
  return x
}
